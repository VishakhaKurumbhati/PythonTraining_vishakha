# > : greater than
# < : less than
# >= : either greater than or equal to
# <= : either less than or equal to
# != : not equal to
# == : equal to

# O/P => Always a boolean value True/False

# left_operand <RO> right_operand

op = 10 > 20  # statement
print(op)

num1, num2, num3, num4 = 1, 2, 3, 2

op = num1 > num2
print(op)

op = num1 < num2
print(op)

op = num1 >= num2
print(op)

op = num2 >= num4
print(op)

op = num1 != num2
print(op)

op = num4 != num2
print(op)

op = num1 = num2  # Assignment
print(op)
print(num1)

op = num1 == num2  # Relational
print(op)

op = num1 = num2  # Assignment
print(op)
print(num1)

op = num1 == num2  # Relational
print(op)